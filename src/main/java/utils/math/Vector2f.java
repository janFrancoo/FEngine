package utils.math;

public class Vector2f {

    public float x, y;

    public Vector2f() {
        this.x = this.y = 0;
    }

    public Vector2f(float value) {
        this.x = value;
        this.y = value;
    }

    public Vector2f(float x, float y) {
        this.x = x;
        this.y = y;
    }

}
